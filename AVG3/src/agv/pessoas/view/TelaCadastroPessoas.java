    /**
     * Dispara o processo de cadastro de um cliente no sistema.
     * @param evt 
     */
    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if (passouNaPreValidacaoCliente()) {
            if (chkFuncionario.isSelected()) {
                if (passouNaPreValidacaoFunc()) {
                    try {
                        cadastraFuncionario();
                        verifSeOUsuarioQuerCadastrNovament();
                    } catch (ValorInvalidoException | ErroCadastroException ex) {
                        JOptionPane.showMessageDialog(rootPane, ex.getMessage(),
                                "Erro", JOptionPane.ERROR_MESSAGE);
                    }
                }
            } else {
                try {
                    cadastraCliente();
                    verifSeOUsuarioQuerCadastrNovament();
                } catch (ValorInvalidoException | ErroCadastroException ex) {
                    JOptionPane.showMessageDialog(rootPane, ex.getMessage(),
                            "Erro", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

