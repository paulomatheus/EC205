    /**
     * Verifica se os campos Nome, CPF e email não ficaram vazios.
     * @return 
     */
    private boolean passouNaPreValidacaoCliente() {
        String nome = txtNome.getText();
        String cpf = txtCpf.getText();
        String email = txtEmail.getText();
        

        if (nome.isEmpty() || cpf.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Nem todos os campos foram "
                    + "preenchidos!", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        } else {
            return true;
        }
    }

    /**
     * Verifica se os campos Login, Senha ou Confirmar Senha ficaram vazios e
     * se o usuário selecionou um cargo e um setor. Também verifica se as senhas
     * digitadas nos campos Senha e Confirmar senha são iguais.
     * @return 
     */
    private boolean passouNaPreValidacaoFunc() {
        String setor = cmbSetor.getSelectedItem().toString();
        String cargo = cmbCargo.getSelectedItem().toString();
        String login = txtLogin.getText();
        String senha = new String(pswSenha.getPassword());
        String confirm_senha = new String(pswConfirmarSenha.getPassword());

        if (login.isEmpty() || senha.isEmpty() || confirm_senha.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Nem todos os campos foram "
                    + "preenchidos!", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (cargo.equals(cmbCargo.getItemAt(0))) { // A combobox está na posição Selecionar.
            JOptionPane.showMessageDialog(rootPane, "Escolha um cargo.", "Erro",
                    JOptionPane.INFORMATION_MESSAGE);
            return false;
        } else if (setor.equals(cmbSetor.getItemAt(0))) { // A combobox está na posição Selecionar.
            JOptionPane.showMessageDialog(rootPane, "Escolha um setor.", "Erro",
                    JOptionPane.INFORMATION_MESSAGE);
            return false;
        } else if (!senha.equals(confirm_senha)) {
            JOptionPane.showMessageDialog(rootPane, "Foram digitadas duas "
                    + "senhas diferentes nos campos senha e confirmar senha!",
                    "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        } else {
            return true;
        }
    }

    /**
     * Lê os dados inserido pelo usuário, coloca os valores obtidos em um objeto
     * Cliente e o envia para o método cadastra da classe ManipulaClientes.
     * @throws ValorInvalidoException
     * @throws ErroCadastroException 
     */
    private void cadastraCliente() throws ValorInvalidoException, ErroCadastroException {
        String nome = txtNome.getText();
        String cpf = txtCpf.getText();
        String email = txtEmail.getText();
        Cliente cliente = new Cliente();

        cliente.setNome(nome);
        cliente.setCpf(cpf);
        cliente.setEmail(email);
        new ManipuladorClientes().cadastra(cliente);
    }

    /**
     * Lê os dados inserido pelo usuário, coloca os valores obtidos em um objeto
     * Funcionario e o envia para o método cadastra da classe ManipulaFuncionarios.
     * @throws ValorInvalidoException
     * @throws ErroCadastroException 
     */
    private void cadastraFuncionario() throws ValorInvalidoException, ErroCadastroException {
        String nome = txtNome.getText();
        String cpf = txtCpf.getText();
        String email = txtEmail.getText();
        String setor = cmbSetor.getSelectedItem().toString();
        String cargo = cmbCargo.getSelectedItem().toString();
        String login = txtLogin.getText();
        String senha = new String(pswSenha.getPassword());
        String confirm_senha = new String(pswConfirmarSenha.getPassword());
        Funcionario func = new Funcionario();

        func.setNome(nome);
        func.setCpf(cpf);
        func.setEmail(email);
        func.setSetor(setor);
        func.setCargo(cargo);
        func.setLogin(login);
        func.setSenha(senha);
        new ManipuladorFuncionarios().cadastra(func);
    }

    private void verifSeOUsuarioQuerCadastrNovament() {
        int respUsuario = JOptionPane.showConfirmDialog(rootPane,
                "Cadastro realizado com sucesso! Deseja cadastrar outra "
                + "pessoa?", "Sucesso!", JOptionPane.YES_NO_OPTION);
        switch (respUsuario) {
            case JOptionPane.YES_OPTION:
                limpaCampos();
                break;
            case JOptionPane.NO_OPTION:
                btnVoltar.doClick();
                break;
            default:
                btnVoltar.doClick();
                break;
        }
    }

    private void limpaCampos() {
        txtNome.setText("");
        txtCpf.setText("");
        txtEmail.setText("");
        if (chkFuncionario.isSelected()) {
            cmbSetor.selectWithKeyChar('s');
            cmbCargo.selectWithKeyChar('s');
            txtLogin.setText("");
            pswSenha.setText("");
            pswConfirmarSenha.setText("");
        }
    }

