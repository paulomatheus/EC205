    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        FuncionarioDao funcDao = new FuncionarioDao();
        ArrayList<Funcionario> funcionarios = funcDao.getLista();
        String loginInformado = txtLogin.getText();
        String senhaInserida = new String(pswSenha.getPassword());
        boolean credenciaisValidas = false;

        for (Funcionario funcionario : funcionarios) {
            if (funcionario.getLogin().equals(loginInformado)
                    && funcionario.getSenha().equals(senhaInserida)) {
                switch (funcionario.getSetor()) {
                    case DOCUMENTOS:
                        TelaInicialDocumentos telaIniDocs;
                        telaIniDocs = new TelaInicialDocumentos(funcionario);
                        telaIniDocs.setLocationRelativeTo(null);
                        telaIniDocs.setVisible(true);
                        credenciaisValidas = true;
                        dispose();
                        break;
                    case EVENTOS:
                        TelaInicialEventos telaIniEvt;
                        telaIniEvt = new TelaInicialEventos(funcionario);
                        telaIniEvt.setLocationRelativeTo(null);
                        telaIniEvt.setVisible(true);
                        credenciaisValidas = true;
                        dispose();
                        break;
                    default:
                        throw new RuntimeException();
                }
            }
        }
        if (!credenciaisValidas) {
            JOptionPane.showMessageDialog(rootPane, "Login invÂ·lido.", "Erro",
                    JOptionPane.ERROR_MESSAGE);
            txtLogin.setText("");
            pswSenha.setText("");
        }
    
    }//GEN-LAST:event_btnEntrarActionPerformed

