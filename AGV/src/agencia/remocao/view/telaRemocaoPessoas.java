    private void buttonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBuscarActionPerformed
        // Ação do botão buscar
        String nome = textFieldNome.getText();
        ChecaInfoPessoas checa = new ChecaInfoPessoas();
        String[] dados = new String[3];
        dados = checa.checarInfo(nome);

        if (dados[0].equals("")) {
            excluir = false;
        } else if (!nome.equals("")) {
            excluir = true; //Sendo verdadeiro é possível alterar a pessoa
            lablelNome.setText(dados[0]);
            labelCPF.setText(dados[1]);
            labelEmail.setText(dados[2]);
        }
    }//GEN-LAST:event_buttonBuscarActionPerformed

