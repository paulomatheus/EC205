    private void buttonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEditarActionPerformed
        // Ação do botão SIM
        ModificaRegistro mod = new ModificaRegistro();

        //Pego o nome que está no campo buscar
        String nome = textFieldNome.getText();

        mod.apagaPessoa(nome);
        textFieldNome.setText("");
        labelCPF.setText("");
        labelEmail.setText("");
        lablelNome.setText("");
        JOptionPane.showMessageDialog(rootPane, "Concluído com sucesso!");
    }//GEN-LAST:event_buttonEditarActionPerformed

